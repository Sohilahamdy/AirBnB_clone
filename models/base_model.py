from datetime import datetime

def parse_iso_format(date_str):
    try:
        return datetime.strptime(date_str, '%Y-%m-%dT%H:%M:%S.%f')
    except ValueError:
        # Handle cases without microseconds
        return datetime.strptime(date_str, '%Y-%m-%dT%H:%M:%S')

